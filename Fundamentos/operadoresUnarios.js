let num1 = 1
let num2 = 2

num1++ //operador de incremento (adiciona 1)
--num1 //operador de decremento (subtrai 1)

console.log("imprimindo Num1", num1)
console.log("imprimindo Num2",num2)

console.log("comparação ++num1 === num2--:  ", ++num1 === num2--) //aqui o prefixo ++ ou -- é mais rápido que usando como sufixo